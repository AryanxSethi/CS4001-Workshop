package week3Tutorial;


public class DataTypeInspector
{
   public static void main(String[]args){
       byte a=127;
       short b=10000;
       int c=1000000;
       long d=100000000L;
       float e=1.111111f;
       double f=1.1111111111d;
       char g='A';
       boolean h=true;
       
       System.out.println("This is Byte: "+a);
       System.out.println("This is Short: "+b);
       System.out.println("This is Integer: "+c);
       System.out.println("This is Long: "+d);
       System.out.println("This is Float: "+e);
       System.out.println("This is Double: "+f);
       System.out.println("This is Character: "+g);
       System.out.println("This is Boolean: "+h);
   }
}